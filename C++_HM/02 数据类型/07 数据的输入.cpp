#include <iostream>
using namespace std;

int main()
{
	// 整型
	int a;
	cout << "请输入a的值：" ;
	cin >> a;
	cout << "a = " << a << endl;

	// 字符型
	char c;
	cout << "请输入c的值：";
	cin >> c;
	cout << "c = " << c << endl;

	// 浮点型
	float b;
	cout << "请输入b的值：";
	cin >> b;
	cout << "b = " << b << endl;

	// 字符串型
	string str;
	cout << "请输入str的值：";
	cin >> str;
	cout << "str = " << str << endl;

	// 布尔型   非零值都是真 （1）

	system("pause");

	return 0;
}